class Node:
    def __init__(self, val):
        self.next = None
        self.children = []
        self.val = val
        self.prev = None

    def add_child(self, node):
            self.children.append(node)

    def add_next(self, node):
            self.next = node
            node.prev = self

    def printme(self):
        if self.val == -1:
            return
        print(self.val, end=" ")
        if self.next is not None:
            self.next.printme()


