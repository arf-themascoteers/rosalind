        if pre_copy == "":
            pre_copy = ele
        elif ele not in pre_copy:
            overlap_length = overlap(pre_copy, ele)
            if overlap_length == 0:
                continue
            pre_copy = pre_copy + ele[overlap_length:]

        dnas_copy = dnas.copy()
        dnas_copy.remove(ele)
        permute(dnas_copy, pre_copy)


