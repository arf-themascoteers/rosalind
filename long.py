def overlap(src, dest):
    str_len = min(len(src), len(dest))
    half_len = str_len // 2

    for overlap_len in range(str_len, -1, -1):
        src_start_index = str_len - overlap_len
        src_end_index = str_len - 1

        dest_start_index = 0
        dest_end_index = overlap_len - 1

        if src[src_start_index: src_end_index+1] == dest[dest_start_index: dest_end_index+1]:
            return overlap_len

    return 0

def superstring(perm):
    cumulative_str = perm[0]
    for i in range(0,len(perm)-1):
        src = cumulative_str
        dest = perm[i+1]

        if dest in src:
            continue

        overlap_length = overlap(src, dest)
        cumulative_str = cumulative_str + dest[overlap_length:]

    return cumulative_str


